public class ServiceDemoImpl implements ServiceDemo{


    private static final String RICE_MERCHANT = "NJUPT1";
    private static final String FISH_MERCHANT = "NJUPT2";
    private static final String FLOWER_MERCHANT = "NJUPT3";


    @Override
    public List<Merchant> getMerchants() {
        return DataHolder.MERCHANTS;
    }

    @Override
    public List<Item> getItems(String merchantId) {
        List<Item> result = new ArrayList<>();
        if (RICE_MERCHANT.equals(merchantId)){
            result = DataHolder.RICE_MERCHANT;
        } else if (FISH_MERCHANT.equals(merchantId)){
            result = DataHolder.FISH_MERCHANT;
        } else if (FLOWER_MERCHANT.equals(merchantId)){
            result = DataHolder.FLOWER_MERCHANT;
        }
        return result;
    }
}
