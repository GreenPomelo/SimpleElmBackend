public class Item {

    /**
     *  商品的表示id
     */
    private String itemId;
    /**
     *  商品的名字
     */
    private String name;
    /**
     *  商品的主要原料
     */
    private String material;
    /**
     *  商品月售多少份
     */
    private int sales;
    /**
     *  商品的价格
     */
    private double money;
    /**
     *  好评率
     */
    private int favorableRate;

    public Item() {
    }

    public Item(String itemId, String name, String material, int sales, double money, int favorableRate) {
        this.itemId = itemId;
        this.name = name;
        this.material = material;
        this.sales = sales;
        this.money = money;
        this.favorableRate = favorableRate;
    }

    public String getItemId() {
        return itemId;
    }

    public void setItemId(String itemId) {
        this.itemId = itemId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public int getSales() {
        return sales;
    }

    public void setSales(int sales) {
        this.sales = sales;
    }

    public double getMoney() {
        return money;
    }

    public void setMoney(double money) {
        this.money = money;
    }

    public int getFavorableRate() {
        return favorableRate;
    }

    public void setFavorableRate(int favorableRate) {
        this.favorableRate = favorableRate;
    }

    @Override
    public String toString() {
        return "Item{" +
                "itemId='" + itemId + '\'' +
                ", name='" + name + '\'' +
                ", material='" + material + '\'' +
                ", sales=" + sales +
                ", money=" + money +
                ", favorableRate=" + favorableRate +
                '}';
    }
}
